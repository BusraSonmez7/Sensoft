public class Bildirimler {
    private int bildirimid;
    private String bildirimbaslik;
    private String bildirimaciklama;
    private String kelime;

    public int getBildirimid() {
        return bildirimid;
    }

    public void setBildirimid(int bildirimid) {
        this.bildirimid = bildirimid;
    }

    public String getBildirimbaslik() {
        return bildirimbaslik;
    }

    public void setBildirimbaslik(String bildirimbaslik) {
        this.bildirimbaslik = bildirimbaslik;
    }

    public String getBildirimaciklama() {
        return bildirimaciklama;
    }

    public void setBildirimaciklama(String bildirimaciklama) {
        this.bildirimaciklama = bildirimaciklama;
    }

    public String getKelime() {
        return kelime;
    }

    public void setKelime(String kelime) {
        this.kelime = kelime;
    }
}
